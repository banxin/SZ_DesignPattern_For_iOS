#import "SZAmativeness.h"

@implementation SZAmativeness

- (NSString *)fetchManConclusion:(SZMan *)man
{
    return @"Man Amativeness 时，凡事不懂也装懂！";
}

- (NSString *)fetchWomanConclusion:(SZWoman *)woman
{
    return @"Woman Amativeness 时，遇事懂也装不懂！";
}

@end
